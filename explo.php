                <p>
                    Exoplanets are planets that orbit stars outside our solar system. NASA has confirmed over 5,600 of these 
                    exoplanets, and it's estimated that billions more exist. Exoplanets come in various types, ranging from 
                    rocky planets like Earth to gas giants similar to Jupiter. Some are located in the so-called "habitable zone" 
                    of their stars, where liquid water could potentially exist, making them interesting candidates in the 
                    search for life. 
                    These planets can be found through different techniques, like the transit method, where a planet passes 
                    in front of its star, causing a small dip in the star's brightness, or radial velocity, which detects the slight 
                    wobble of a star caused by the gravitational pull of an orbiting planet. Upcoming NASA missions, like the 
                    James Webb Space Telescope and others, will help scientists study exoplanets' atmospheres and 
                    compositions in more detail, aiming to identify planets that could potentially support life. 
                    Discoveries range from rocky planets with potential Earth-like conditions to gas giants orbiting incredibly 
                    close to their stars. The search continues for exoplanets with the right conditions for habitability, and 
                    many exciting findings are still to come.
                </p>
