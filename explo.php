                <p> 
                    <h>Types of Exoplanets </h>
                    <dl>
                        <dt><h>Super-Earths </h></dt>
                        <dd> Are exoplanets with a mass larger than Earth's but significantly smaller than that of 
                            Neptune. They can be rocky or have substantial atmospheres and are considered potential candidates 
                            for habitability due to their size and position in the habitable zone of their stars. 
                        </dd>

                        <dt><h>Gas Giants </h></dt>
                        <dd> Gas giants are large planets predominantly composed of hydrogen and helium, with little to no solid 
                            surface. They are significantly larger than Earth and can have complex atmospheres with thick clouds 
                            and storms. Jupiter and Saturn in our solar system are classic examples. 
                        </dd>

                        <dt><h> Neptunian Planets</h></dt>
                        <dd>  Are like gas giants but are typically smaller and have a higher concentration of ices, 
                            such as water, ammonia, and methane. They may have thick atmospheres and are often categorized 
                            based on their specific characteristics.
                        </dd>

                        <dt><h> Terrestrial Planets</h></dt>
                        <dd> Also known as rocky planets, have solid surfaces and are composed primarily of rock 
                            and metal. These planets are usually smaller and can support a variety of geological processes. Examples 
                            include Earth and Mars in our solar system.
                        </dd>
                    </dl>

